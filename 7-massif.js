// Масиви
// Масиви — це спеціальний тип об'єктів, що дозволяє зберігати
//  упорядковані колекції елементів.Вони часто використовуються
//  для роботи з наборами даних.

// 1. Оголошення масивів
// 1.1. Створення через квадратні дужки []
// Найпоширеніший спосіб створення масивів.

// const fruits = ["яблуко", "банан", "вишня"];
// console.log(fruits); // Виведе: ["яблуко", "банан", "вишня"]

// 1.2. Створення через конструктор Array
const fruitss = new Array("яблуко", "банан", "вишня");
console.log(fruitss); // Виведе: ["яблуко", "банан", "вишня"]

// 1.3. Порожній масив
const emptyArray = [];
console.log(emptyArray); // Виведе: []

// 2. Доступ до елементів масиву
// Елементи масиву доступні за індексами, які починаються з нуля.

const fruitts = ["яблуко", "банан", "вишня"];
console.log(fruitts[0]); // Виведе: яблуко
console.log(fruitts[2]); // Виведе: вишня

// 3. Основні властивості масивів
// 3.1. length
// Властивість length повертає кількість елементів у масиві.

const fruiits = ["яблуко", "банан", "вишня"];
console.log(fruiits.length); // Виведе: 3

// 4. Методи масивів
// 4.1. Додавання та видалення елементів
// push() — додає елемент(и) в кінець масиву.
const fruuits = ["яблуко", "банан"];
fruits.push("вишня");
console.log(fruuits); // Виведе: ["яблуко", "банан", "вишня"]

// pop() — видаляє останній елемент.
const frruits = ["яблуко", "банан", "вишня"];
fruits.pop();
console.log(frruits); // Виведе: ["яблуко", "банан"]

// unshift() — додає елемент(и) на початок.
const ffruits = ["яблуко", "банан"];
fruits.unshift("вишня");
console.log(ffruits); // Виведе: ["вишня", "яблуко", "банан"]

// shift() — видаляє перший елемент.
const fruits1 = ["яблуко", "банан", "вишня"];
fruits1.shift();
console.log(fruits1); // Виведе: ["банан", "вишня"]

// 4.2. Пошук в масиві
// indexOf() — повертає індекс першого входження елемента або -1, якщо елемент не знайдений.
const fruits2 = ["яблуко", "банан", "вишня"];
console.log(fruits2.indexOf("банан")); // Виведе: 1
console.log(fruits2.indexOf("груша")); // Виведе: -1

// includes() — перевіряє, чи є елемент у масиві.
const fruits3 = ["яблуко", "банан", "вишня"];
console.log(fruits3.includes("банан")); // Виведе: true
console.log(fruits3.includes("груша")); // Виведе: false

// 4.3. Маніпуляції з масивами
// slice() — повертає новий масив, який є підмасивом існуючого.
const fruits4 = ["яблуко", "банан", "вишня", "груша"];
console.log(fruits4.slice(1, 3)); // Виведе: ["банан", "вишня"]

// splice() — змінює масив, додаючи або видаляючи елементи.
const fruits5 = ["яблуко", "банан", "вишня"];
fruits5.splice(1, 1, "груша");
console.log(fruits5); // Виведе: ["яблуко", "груша", "вишня"]

// concat() — об'єднує два або більше масивів.
const fruits6 = ["яблуко", "банан"];
const moreFruits6 = ["вишня", "груша"];
console.log(fruits6.concat(moreFruits6)); // Виведе: ["яблуко", "банан", "вишня", "груша"]

// 4.4. Сортування
// sort() — сортує масив за алфавітом.
const fruits7 = ["вишня", "яблуко", "банан"];
console.log(fruits7.sort()); // Виведе: ["банан", "вишня", "яблуко"]

// reverse() — змінює порядок елементів на зворотний.
const fruits8 = ["яблуко", "банан", "вишня"];
console.log(fruits8.reverse()); // Виведе: ["вишня", "банан", "яблуко"]

//4.5. Ітерація
// forEach() — виконує функцію для кожного елемента.
const fruits9 = ["яблуко", "банан", "вишня"];
fruits.forEach((fruit) => console.log(fruit));
// Виведе: яблуко
//         банан
//         вишня

// map() — створює новий масив на основі результатів виклику функції для кожного елемента.
const numbers1 = [1, 2, 3];
const squares = numbers.map((num) => num * num);
console.log(squares); // Виведе: [1, 4, 9]

// filter() — повертає новий масив із елементами, які відповідають умові.
const numbers2 = [1, 2, 3, 4];
const evenNumbers = numbers2.filter((num) => num % 2 === 0);
console.log(evenNumbers); // Виведе: [2, 4]

// reduce() — зводить масив до одного значення.
const numbers = [1, 2, 3, 4];
const sum = numbers.reduce((acc, num) => acc + num, 0);
console.log(sum); // Виведе: 10

// 5. Спеціальні можливості
// 5.1. Деструктуризація
const fruits0 = ["яблуко", "банан", "вишня"];
const [first, second] = fruits0;
console.log(first); // Виведе: яблуко
console.log(second); // Виведе: банан

// 5.2. Spread оператор ...
// Розпаковка масиву.
const fruits = ["яблуко", "банан"];
const moreFruits = [...fruits, "вишня"];
console.log(moreFruits); // Виведе: ["яблуко", "банан", "вишня"]

// 6. Особливості масивів
// Масиви в JavaScript можуть містити елементи різних типів:
const mixedArray = [1, "два", true, null];
console.log(mixedArray); // Виведе: [1, "два", true, null]

// Масиви динамічно змінюють свій розмір.
// 7. Перевірка, чи це масив
// Для перевірки, чи є змінна масивом,
// використовується метод Array.isArray().

console.log(Array.isArray([1, 2, 3])); // Виведе: true
console.log(Array.isArray("текст")); // Виведе: false
